const input = document.getElementById('busqueda');
const sugerencias = document.getElementById('sugerencias');
const form = document.getElementById('form-busqueda');

input.addEventListener('input', function() {
    const valor = input.value.trim().toLowerCase();
    sugerencias.innerHTML = '';
    if (valor.length === 0) {
        sugerencias.style.display = 'none';
        return;
    }

    const palabrasBuscadas = valor.split(' ').filter(Boolean);
    const coincidencias = productos.filter(p => {
        const palabrasProducto = p.nombre.toLowerCase().split(' ');
        return palabrasBuscadas.every(palabra =>
            palabrasProducto.some(word => word.startsWith(palabra))
        );
    });
    if (coincidencias.length > 0) {
        coincidencias.slice(0, 12).forEach(p => {
            const div = document.createElement('div');
            div.textContent = p.nombre;
            div.setAttribute('translate', 'no');
            div.style.cursor = 'pointer';
            div.style.padding = '5px';
            div.addEventListener('mousedown', function(e) {
                e.preventDefault();
                window.location.href = p.url;
            });
            sugerencias.appendChild(div);
        });
        sugerencias.style.display = 'block';
    } else {
        sugerencias.style.display = 'none';
    }
});

input.addEventListener('blur', function() {
    setTimeout(() => sugerencias.style.display = 'none', 100);
});

form.addEventListener('submit', function(e) {
    e.preventDefault();
    const valor = input.value.trim().toLowerCase();
    const producto = productos.find(p => p.nombre.toLowerCase() === valor);
    if (producto) {
        window.location.href = producto.url;
    } else {
        alert('No se encontró el producto.');
    }
});

document.getElementById('openSearch').addEventListener('click', function() {
    var overlay = document.getElementById('searchOverlay');
    overlay.style.display = 'flex';
    document.getElementById('busqueda').focus();
});

document.getElementById('closeSearch').addEventListener('click', function() {
    document.getElementById('searchOverlay').style.display = 'none';
    document.getElementById('busqueda').value = '';
    sugerencias.style.display = 'none';
});

document.getElementById('searchOverlay').addEventListener('click', function(e) {
    if (e.target === this) {
        this.style.display = 'none';
        document.getElementById('busqueda').value = '';
        sugerencias.style.display = 'none';
    }
});
